//3. What is var? Should we use our old friend ‘var’?
/*
declare a JavaScript variable with the var keyword.
After the declaration, the variable has no value (technically it has the value
of undefined).
To assign a value to the variable, use the equal sign.
we can also assign a value to the variable when we declare it.
we can declare many variables in one statement.
Start the statement with var and separate the variables by comma.
If you re-declare a JavaScript variable, it will not lose its value.
Remember that JavaScript identifiers (names) must begin with:
 A letter (A-Z or a-z)
 A dollar sign ($)
 Or an underscore (_)
*/

var $$$ = "Hello World";
var $ = 2;
var $myMoney = 5;

var _lastName = "Desai";
var _x = 2;
var _100 = 5;

console.log($$$);
console.log($);
console.log($myMoney);

console.log(_lastName);
console.log(_x);
console.log(_100);